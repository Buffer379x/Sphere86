"""
Hardware lists for 86Box configuration.

Loads from the database generated by parse_86box.py (86box_hardware_db.json).

Load order:
  1. {data_path}/config/86box_hardware_db.json  (runtime-generated, preferred)
  2. app/86box_hardware_db.json                (bundled fallback)
"""

import json
import logging
from functools import lru_cache
from pathlib import Path

log = logging.getLogger("Sphere86.hardware_lists")

_HERE = Path(__file__).parent

# Machine flag constants (from 86Box source)
MACHINE_FLAG_VIDEO      = 0x00000002
MACHINE_FLAG_VIDEO_ONLY = 0x00000008
MACHINE_FLAG_SOUND      = 0x00020000
MACHINE_FLAG_HDC        = 0x0001F100
MACHINE_FLAG_FDC        = 0x00080000
MACHINE_FLAG_NIC        = 0x00100000


@lru_cache(maxsize=1)
def _load() -> dict:
    """Load and return the raw hardware database."""
    paths = []

    # 1. Runtime-generated cache
    try:
        from .config import get_settings
        paths.append(Path(get_settings().data_path) / "config" / "86box_hardware_db.json")
    except Exception:
        pass

    # 2. Bundled fallback (next to this file)
    paths.append(_HERE / "86box_hardware_db.json")

    for p in paths:
        if p.exists():
            log.debug("Loading hardware database from %s", p)
            return json.loads(p.read_text(encoding="utf-8"))

    raise FileNotFoundError(
        "86box_hardware_db.json not found. "
        "It will be generated automatically on first startup."
    )


def reload() -> None:
    """Clear the in-memory cache so the next call re-reads from disk."""
    _load.cache_clear()


# ─── Device list normalisation ────────────────────────────────────────────────

def _norm_config_entry(c: dict) -> dict:
    """Translate raw parser config entry to the schema expected by the frontend."""
    out = dict(c)
    # Strip CONFIG_ prefix and lowercase: CONFIG_HEX16 → hex16
    raw_type = c.get("type", "")
    out["type"] = raw_type.removeprefix("CONFIG_").lower()
    # default_int → default
    if "default_int" in c:
        out["default"] = c["default_int"]
        del out["default_int"]
    # selection → options (frontend expects 'options')
    if "selection" in c:
        out["options"] = c["selection"]
        del out["selection"]
    # spinner.{min,max,step} → spinner_min/max/step
    sp = c.get("spinner")
    if sp and (sp.get("min") or sp.get("max")):
        out["spinner_min"] = sp["min"]
        out["spinner_max"] = sp["max"]
        out["spinner_step"] = sp.get("step", 1)
    if "spinner" in out:
        del out["spinner"]
    # bios entries are complex and not rendered — drop to avoid noise
    out.pop("bios", None)
    return out


def _norm(devices: list[dict]) -> list[dict]:
    """Normalise device list: expose internal_name as 'id', translate config entries."""
    out = []
    for d in devices:
        entry = dict(d)
        entry["id"] = d.get("internal_name", d.get("id", ""))
        if "config" in entry:
            entry["config"] = [_norm_config_entry(c) for c in entry["config"]]
        out.append(entry)
    return out


