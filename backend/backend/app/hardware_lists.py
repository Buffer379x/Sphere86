# ─── Config-generation helpers ────────────────────────────────────────────────

def machine_has_builtin_video(machine_id: str) -> bool:
    """Return True if this machine has built-in / fixed video."""
    for m in get_machines():
        if m.get("internal_name") == machine_id:
            flags = m.get("flags_value", 0)
            return bool(flags & (MACHINE_FLAG_VIDEO | MACHINE_FLAG_VIDEO_ONLY))
    return False


def get_cpu_by_index(cpu_family_id: str, speed_index: int) -> tuple[int, int]:
    """
    Return (rspeed_hz, multi) for the given cpu_family and speed index.

    rspeed_hz is written as 'cpu_speed' in 86Box cfg.
    multi is written as 'cpu_multi'.

    Falls back to the first CPU in the family, or (4772728, 1) if not found.
    """
    for fam in get_cpu_families():
        if fam.get("internal_name") == cpu_family_id:
            cpus = fam.get("cpus", [])
            if not cpus:
                break
            idx = max(0, min(speed_index, len(cpus) - 1))
            cpu = cpus[idx]
            return (int(cpu.get("rspeed", 4772728)), int(cpu.get("multi", 1)))
    return (4772728, 1)


def get_cpu_families_for_machine(machine_id: str) -> list[dict]:
    """Return CPU families compatible with the given machine."""
    machine = None
    for m in get_machines():
        if m.get("internal_name") == machine_id:
            machine = m
            break
    if machine is None:
        return []

    pkg_set = set(machine.get("cpu_packages", []))
    # A family's package may be a compound string like "CPU_PKG_SOCKET1 | CPU_PKG_SOCKET3_PC330",
    # meaning the CPU fits in any of those sockets. Split and check for any overlap.
    def _family_matches(f: dict) -> bool:
        fam_pkgs = {p.strip() for p in f.get("package", "").split("|")}
        return bool(fam_pkgs & pkg_set)
    return [f for f in get_cpu_families() if _family_matches(f)]


